// Global in-page toast notification service
// Usage: AppToast.show('message')  AppToast.show('message', 3000)
const AppToast = (() => {
    let container = null;
    let _timer = null;

    function _ensureContainer() {
        if (container) return;
        container = document.createElement('div');
        container.id = 'app-toast';
        container.style.cssText = `
            position: fixed;
            top: 50%;
            left: 50%;
            transform: translate(-50%, -50%) scale(0.9);
            background: rgba(30, 30, 30, 0.88);
            color: #fff;
            padding: 14px 28px;
            border-radius: 12px;
            font-size: 17px;
            font-weight: 500;
            z-index: 9999;
            pointer-events: none;
            opacity: 0;
            transition: opacity 0.25s ease, transform 0.25s ease;
            max-width: 80vw;
            text-align: center;
            line-height: 1.5;
            box-shadow: 0 6px 24px rgba(0,0,0,0.35);
        `;
        document.body.appendChild(container);
    }

    function show(message, duration = 2200) {
        _ensureContainer();
        container.textContent = message;

        // Trigger show
        // Use rAF to ensure transition fires after initial paint
        requestAnimationFrame(() => {
            container.style.opacity = '1';
            container.style.transform = 'translate(-50%, -50%) scale(1)';
        });

        // Clear previous hide timer
        if (_timer) clearTimeout(_timer);

        _timer = setTimeout(() => {
            container.style.opacity = '0';
            container.style.transform = 'translate(-50%, -50%) scale(0.9)';
        }, duration);
    }

    return { show };
})();

window.AppToast = AppToast;

